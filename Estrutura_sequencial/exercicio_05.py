"""
Faça um programa que converta metros para centímetros:
"""

# Solicita o valor em metros ao usuário
metros = float(input("Entre com os métros: "))

# Realiza a conversão (1 metro = 100 centímetros)
centimetros = metros * 100

# Exibe o resultado de forma clara ao usuário
print(f"{metros} metros equivalem a {centimetros:.0f} em centímetros")
